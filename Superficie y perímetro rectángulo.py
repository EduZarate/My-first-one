""" Implementar  un  algoritmo  que  permita  ingresar  las  longitudes
de  un rectángulo y muestre por pantalla el perímetro y la superficie
de la figura."""

a = int(input("Ingrese la base del rectángulo: "))
b = int(input("Ingrese la altura del rectángulo: "))
print ("La superficie del rectángulo es: ",a*b)
print ("El perímetro del rectángulo es: ", a*2 + b*2)

